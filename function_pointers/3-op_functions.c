#include "3-calc.h"
/**
 * op_add - plus
 * op_sub - moins
 * op_mul - multiplier
 * op_div - diviser
 * op_mod - reste de la division
 * Return: ooo
 */
int op_add(int a, int b)
{
	return (a + b);
}
int op_sub(int a, int b)
{
	return (a - b);
}
int op_mul(int a, int b)
{
	return (a * b);
}
int op_div(int a, int b)
{
	return (a / b);
}
int op_mod(int a, int b)
{
	return (a % b);
}
